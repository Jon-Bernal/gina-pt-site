<section>
	<h3>What is Fascial Counterstrain?</h3>
	<p>
		Fascial Counterstrain (FCS) is the application of hands-on manual therapy to release
		contractions of your tissues, such as muscles, ligaments, and nerves. When there is tissue
		tension, this can cause pain and dysfunction. By releasing these tissues, the body can move more
		appropriately and release inflammation. This is a painless technique and has the ability to
		affect any system in the body, including lymphatics, visceral organs, bone, and more. Learn more
		about FCS here.
	</p>
</section>

